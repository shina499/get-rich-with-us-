let toastEl = document.getElementById("toast-warning"); // اصلاح شد
let msgEl = document.getElementById("msg"); // اصلاح شد

function Show_Toast() {
    toastEl.style.display = "flex"; // نمایش Toast
    msgEl.innerHTML = TEXTS[Math.floor(Math.random() * TEXTS.length)]; // نمایش پیام
    setTimeout(hide_Toast, 10000); // بستن Toast بعد از 10 ثانیه
}

function hide_Toast() {
    toastEl.style.display = "none"; // مخفی کردن Toast
}

// بستن Toast با کلیک روی دکمه
document.getElementById("close").addEventListener("click", function () {
    hide_Toast();
});

// نمایش Toast به صورت خودکار هر 15 ثانیه
